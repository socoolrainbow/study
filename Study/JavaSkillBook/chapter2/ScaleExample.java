package chapter2;

public class ScaleExample
{
    public static void main(String[] args)
    {
        // 10진수
        int decimal = 12;
        // 16진수
        int hexadecimal = 0XC;
        // 8진수
        int octet = 014;
        // 2진수
        int binary = 0B1100;
        
        System.out.println(decimal);
        System.out.println(hexadecimal);
        System.out.println(octet);
        System.out.println(binary);
    }
}
